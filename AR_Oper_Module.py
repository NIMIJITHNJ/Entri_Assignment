def addition(a,b):
    add = a+b
    return add
def subtract(a,b):
    sub = a-b
    return sub
def multiply(a,b):
    multi =a*b
    return multi
def divide(a,b):
    div =a/b
    return div
def divide1(a,b):
    div1 = a//b
    return div1
def modulus(a,b):
    mod = a%b
    return mod
def exponent(a,b):
    exp = a**b
    return exp